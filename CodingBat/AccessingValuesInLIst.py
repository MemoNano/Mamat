"""
To Access values in lits, use the square brackets for slicing along with the index or indices to obtain
value available at that index. For example -

"""
# Lists use paranthesis ()
list1 = ['physics', 'chemistry', 1997, 2000]
list2 = [1, 2, 3, 4, 5, 6, 7]
print("list1[0]: ", list1[0])
print("list2[1:5]: ", list2[1:5])

